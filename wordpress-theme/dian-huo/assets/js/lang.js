function applyLang(lang) {
  document.documentElement.lang = lang === 'zh' ? 'zh-TW' : 'en';
  document.documentElement.classList.toggle('lang-zh', lang === 'zh');
  document.documentElement.classList.toggle('lang-en', lang !== 'zh');
  localStorage.setItem('dh-lang', lang);
  var t = TRANSLATIONS[lang];

  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.dataset.i18n;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    var key = el.dataset.i18nPh;
    if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
  });

  document.querySelectorAll('[data-i18n-opt]').forEach(function(el) {
    var key = el.dataset.i18nOpt;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  var btn = document.getElementById('langToggle');
  if (btn) btn.textContent = lang === 'zh' ? 'EN' : '中文';
}

(function() {
  var saved = localStorage.getItem('dh-lang') || 'en';
  applyLang(saved);

  var btn = document.getElementById('langToggle');
  if (btn) {
    btn.addEventListener('click', function() {
      var cur = localStorage.getItem('dh-lang') || 'en';
      applyLang(cur === 'en' ? 'zh' : 'en');
    });
  }
})();
